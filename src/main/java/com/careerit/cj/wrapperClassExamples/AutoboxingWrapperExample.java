package com.careerit.cj.wrapperClassExamples;

public class AutoboxingWrapperExample {
    public static void main(String[] args) {
        Integer wrappedNum = 10;
        int num = wrappedNum;
        System.out.println("Wrapped num: " + wrappedNum);
        System.out.println("Unwrapped num: " + num);
    }
}
