package com.careerit.cj.day17.Inheritance2;

public class Animal {
    String name;

    public void eat() {
        System.out.println("Animal is eating");
    }
    public void sleep() {
        System.out.println("Animal is sleeping");
    }
    public void run() {
        System.out.println("Animal is running");
    }
    public void sound() {
        System.out.println("Animal is making sound");
    }
}
