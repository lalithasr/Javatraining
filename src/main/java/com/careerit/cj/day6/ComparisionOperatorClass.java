package com.careerit.cj.day6;

public class ComparisionOperatorClass {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        System.out.println("Is x is greater than y : " + (x > y));
        System.out.println("Is x is less than y : " + (x < y));
        System.out.println("Is x is greater than or equal to y : " + (x >= y));
        System.out.println("Is x is less than or equal to y : " + (x <= y));
        System.out.println("Is x is equal to y : " + (x == y));
        System.out.println("Is x is not equal to y : " + (x != y));
    }
}
